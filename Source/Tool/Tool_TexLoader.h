#pragma once

//===== インクルード部 =====
#include <Win/Win_DirectX.h>

//===== クラス宣言 =====

//***** テクスチャ読込 *****
class ToolTexLoader
{
public:

    //テクスチャ情報
    struct TexData
    {
        //----------------------------------------------------------------------

        //----------------------------------------------------------------------
        /// コンストラクタ
        ///
        /// \return void
        //----------------------------------------------------------------------
        TexData() noexcept : pImageData(nullptr), nWidth(0), nHeight(0)
        {}

        //----------------------------------------------------------------------
        /// デストラクタ
        ///
        /// \return void
        //----------------------------------------------------------------------
        ~TexData() noexcept {}

        //----------------------------------------------------------------------

        //----------------------------------------------------------------------
        unsigned char* pImageData;
        int nWidth;
        int nHeight;
        //----------------------------------------------------------------------

        /// <summary>
        /// pImageData  データポインタ
        /// nWidth      画像幅
        /// nHeight     画像高さ
        /// </summary>
    };

private:

    //--------------------------------------------------------------------------

    //--------------------------------------------------------------------------
    /// コンストラクタ
    ///
    /// \return void
    //--------------------------------------------------------------------------
    ToolTexLoader() noexcept {}

    //--------------------------------------------------------------------------
    /// デストラクタ
    ///
    /// \return void
    //--------------------------------------------------------------------------
    ~ToolTexLoader() noexcept {}

    //--------------------------------------------------------------------------
};
