#pragma once

//===== インクルード部 =====
#include <string>

//===== クラス定義 =====
class WinWindow
{
public:

    //--------------------------------------------------------------------------

    //--------------------------------------------------------------------------
    /// コンストラクタ
    ///
    /// \return void
    //--------------------------------------------------------------------------
    WinWindow();

    //--------------------------------------------------------------------------
    /// デストラクタ
    ///
    /// \return void
    //--------------------------------------------------------------------------
    virtual ~WinWindow() noexcept(false);

    //--------------------------------------------------------------------------
    /// トランスフォーム
    ///
    /// \param[in] nWndPosX     ウィンドウの左上位置（x軸）
    /// \param[in] nWndPosY     ウィンドウの左上位置（y軸）
    /// \param[in] nWndWidth    ウィンドウ幅（クライアント領域）
    /// \param[in] nWndHeight   ウィンドウ高さ（クライアント領域）
    ///
    /// \return void
    //--------------------------------------------------------------------------
    virtual void Transform(
        /*[in]*/ int nWndPosX,
        /*[in]*/ int nWndPosY,
        /*[in]*/ int nWndWidth = 0,
        /*[in]*/ int nWndHeight = 0) = 0;

    //--------------------------------------------------------------------------
    /// タイトル出力
    ///
    /// \param[in] text     出力文字列
    ///
    /// \return void
    //--------------------------------------------------------------------------
    virtual void TitlePrint(
        /*[in]*/ const std::string& text) const = 0;

    //--------------------------------------------------------------------------
    /// タイトル出力（マウス座標）
    ///
    /// \return void
    //--------------------------------------------------------------------------
    virtual void TitlePrint_MousePos() const = 0;

    //--------------------------------------------------------------------------
    /// タイトル出力（ホイール値）
    ///
    /// \return void
    //--------------------------------------------------------------------------
    virtual void TitlePrint_WheelVal() = 0;

    //--------------------------------------------------------------------------
    /// マウス使用
    ///
    /// \return void
    //--------------------------------------------------------------------------
    virtual void EnableCursor() noexcept = 0;

    //--------------------------------------------------------------------------
    /// マウス不使用
    ///
    /// \return void
    //--------------------------------------------------------------------------
    virtual void DisableCursor() noexcept = 0;

    //--------------------------------------------------------------------------
    /// マウス使用状態確認
    ///
    /// \return bool型変数(マウスが使用中かどうか)
    //--------------------------------------------------------------------------
    virtual bool IsUsingCursor() const noexcept = 0;

    //--------------------------------------------------------------------------
    /// ウィンドウ左上座標取得
    ///
    /// \return ウィンドウ左上座標(int, int)
    //--------------------------------------------------------------------------
    virtual std::pair<int, int> GetPos() const noexcept = 0;

    //--------------------------------------------------------------------------
    /// ウィンドウサイズ取得
    ///
    /// \return ウィンドウサイズ
    //--------------------------------------------------------------------------
    virtual std::pair<int, int> GetSize() const noexcept = 0;

    //--------------------------------------------------------------------------
    /// Imgui使用確認
    ///
    /// \return bool型変数(Imguiが使用中かどうか)
    //--------------------------------------------------------------------------
    virtual const bool IsUsingImgui() const noexcept = 0;

    //--------------------------------------------------------------------------
};
